#include <iostream>

using namespace std;

int main()
{
	string character_name = "John";
	int character_age;
	character_age = 35;
	cout << "He's "<< character_name << endl;
	cout << "He was " << character_age << " years old" << endl;

	character_name = "Mike";
	character_age = 22;
	cout << "He's "<< character_name << endl;
	cout << "He was " << character_age << " years old" << endl;

	return 0;
}
